    public void InsertUser(String nss,String name, String app, String apm, String dom, String tel) throws SQLException{
        Statement stm = reg.createStatement();
        
        stm.executeUpdate("INSERT INTO `staff` (`NSS`,`nombre`, `telefono`, `domicilio`, `fecha_nac`,`fecha_in`) VALUES ('"+nss+"','"+name+"', '"+tel+"', '"+ dom +"', '"+app+"', '"+ apm +"')");
        javax.swing.JOptionPane.showMessageDialog(this, "¡Empleado registrado correctamente! \n", "HECHO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
    }
    
    public void EditUser(String id, String name, String app, String apm, String dom, String tel) throws SQLException{
        Statement stm = reg.createStatement();
        
        stm.executeUpdate("UPDATE `staff` SET `nombre` = '"+name+"', `tel` = '"+tel+"', `domicilio` = '"+dom+"', `fecha_nac` = '"+app+"', `fecha_in` = '"+apm+"' WHERE `NSS` = "+ id +";");
        javax.swing.JOptionPane.showMessageDialog(this, "¡Usuario editado correctamente! \n", "HECHO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
    }

