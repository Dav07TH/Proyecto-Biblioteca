    private void buttonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonMousePressed
        if( NSS.getText().equals("Ingrese el NSS") || name.getText().equals("Ingrese el nombre") || ap1.getText().equals("Ingrese la fecha de nacimiento")
            || ap2.getText().equals("Ingrese la fecha de inicio en que se contrato") || dom.getText().equals("Ingrese el domicilio")
            || tel.getText().equals("Ingrese un número telefónico")){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            name.requestFocus();
        }
        else
        {
            String unss = NSS.getText();
            String uname = name.getText();
            String uapp = ap1.getText();
            String uapm = ap2.getText();
            String udom = dom.getText();
            String utel = tel.getText();

            if(unss == null ||uname == null || "".equals(uname) || uapp == null || "".equals(uapp) || uapm == null || "".equals(uapm) || udom == null || "".equals(udom) || utel == null || "".equals(utel)){
                javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                name.requestFocus();
            }
            else{ 
                try {
                    if(edit)
                        EditUser(idus, uname, uapp, uapm, udom, utel);
                    else
                        InsertUser(unss, uname, uapp, uapm, udom, utel);

                    NSS.setText("");
                    name.setText("");
                    ap1.setText("");
                    ap2.setText("");
                    dom.setText("");
                    tel.setText("");

                    if(edit){
                        Empleados p1 = new Empleados();
                        p1.setSize(750, 430);
                        p1.setLocation(0, 0);

                        contenido.removeAll();
                        contenido.add(p1, BorderLayout.CENTER);
                        contenido.revalidate();
                        contenido.repaint();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(UpEmpleados.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_buttonMousePressed

